package Day10;

public abstract class Phone {
	//abstract 추상클래스 선언
	
	//1. 필드
	public String owner;
	
	//2.생성자
	public Phone(String owner) {
		this.owner = owner;
	}
	//3.메소드
	public void turnon() {
		System.out.println("폰 전원을 켭니다");
	}
	public void turnoff() {
		System.out.println("폰 전원을 끕니다");
	}
	
}
